#include <bits/stdc++.h>

#include "atcoder.h"

void Main() {
  ints(m, n);
  V<array<int, 5>> intro = {{3, 6, 8, 8, 8},
                            {6, 18, 20, 16, 16},
                            {8, 20, 28, 16, 18},
                            {8, 16, 16, 18, 20},
                            {8, 16, 18, 20, 18},
                            {8, 18, 20, 18, 16},
                            // Loop starts here.
                            {9, 20, 18, 16, 18},
                            {10, 18, 16, 18, 20},
                            {9, 16, 18, 20, 18},
                            {8, 18, 20, 18, 16}};
  V<array<int, 4>> loop = {{8, 9, 10, 9},
                           {18, 20, 18, 16},
                           {20, 18, 16, 18},
                           {18, 16, 18, 20},
                           {16, 18, 20, 18},
                           {18, 20, 18, 16},
                           // Loop starts here.
                           {20, 18, 16, 18},
                           {18, 16, 18, 20},
                           {16, 18, 20, 18},
                           {18, 20, 18, 16},
                           {20, 18, 16, 18}};
  if (m >= 7) m = (m - 7) % 4 + 7;
  if (n <= 5) {
    wt(intro[m - 1][n - 1]);
  } else {
    wt(loop[m - 1][(n - 6) % 4]);
  }
}
